export function getStoredLanguage(): Language {
  try {
    const stored = localStorage.getItem('bs_language');
    if (stored && (stored === 'en' || stored === 'hi' || stored === 'hinglish' || stored === 'gu')) return stored;
  } catch {}
  return 'hinglish';
}

export function setStoredLanguage(lang: Language) {
  try { localStorage.setItem('bs_language', lang); } catch {}
}

export function hasChosenLanguage(): boolean {
  try {
    return !!localStorage.getItem('bs_language');
  } catch {}
  return false;
}
